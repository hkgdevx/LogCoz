import stripAnsi from 'strip-ansi';

/**************************************************************************************************************************
 IMPLEMENTATIONS
***************************************************************************************************************************/

export function normalizeLog(input: string): string {
  return stripAnsi(input)
    .replace(/\r\n/g, '\n')
    .replace(/\t/g, '  ')
    .replace(/\n{3,}/g, '\n\n')
    .trim();
}
