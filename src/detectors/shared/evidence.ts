import { MAX_EVIDENCE_LINES } from '@/constants/defaults';

/**************************************************************************************************************************
 IMPLEMENTATIONS
***************************************************************************************************************************/
export function pickEvidence(input: string, patterns: RegExp[]): string[] {
  return input
    .split('\n')
    .filter((line) => patterns.some((pattern) => pattern.test(line)))
    .slice(0, MAX_EVIDENCE_LINES);
}
